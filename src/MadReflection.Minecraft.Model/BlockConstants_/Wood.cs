		public const int StrippedSpruce = Stripped + Spruce;
		public const int StrippedBirch = Stripped + Birch;
		public const int StrippedJungle = Stripped + Jungle;
		public const int StrippedAcacia = Stripped + Acacia;
		public const int StrippedDarkOak = Stripped + DarkOak;
	}
}
